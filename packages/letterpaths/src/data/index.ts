const letterVariantsByEntry: Record<CursiveEntryVariant, Record<string, BezierLetter>> = {
  "entry-low": entryLowLetters,
  "entry-high": entryHighLetters
};

const defaultLetterList = Object.values(
  letterVariantsByEntry[defaultCursiveEntryVariant]
) as BezierLetter[];

export const letters = defaultLetterList;
export const printLetters = Object.values(printLettersByChar) as BezierLetter[];

export function createLetterId(
  char: string,
  entryVariant: CursiveEntryVariant = defaultCursiveEntryVariant
): string {
  return `${char}-lower-cursive-${entryVariant}`;
}

export function createLegacyLetterId(char: string): string {
  return `${char}-lower-cursive`;
}

export function createPrintLetterId(char: string): string {
  return `${char}-lower-print`;
}

export function getCursiveLetterVariant(
  char: string,
  entryVariant: CursiveEntryVariant = defaultCursiveEntryVariant
): BezierLetter | null {
  return letterVariantsByEntry[entryVariant][char] ?? null;
}

export function getPrintLetter(char: string): BezierLetter | null {
  return printLettersByChar[char] ?? null;
}

export const lettersById = Object.fromEntries(
  defaultLetterList.map((letter) => [createLegacyLetterId(letter.glyph.char), letter])
) as Record<string, BezierLetter>;

export const printLettersById = Object.fromEntries(
  printLetters.map((letter) => [createPrintLetterId(letter.glyph.char), letter])
) as Record<string, BezierLetter>;

const cursiveLettersByVariantId = Object.fromEntries(
  (
    Object.entries(letterVariantsByEntry) as Array<
      [CursiveEntryVariant, Record<string, BezierLetter>]
    >
  ).flatMap(([entryVariant, lettersForEntry]) =>
    Object.values(lettersForEntry).map((letter) => [
      createLetterId(letter.glyph.char, entryVariant),
      letter
    ])
  )
) as Record<string, BezierLetter>;

export const lettersByVariantId = {
  ...cursiveLettersByVariantId,
  ...printLettersById
} as Record<string, BezierLetter>;
